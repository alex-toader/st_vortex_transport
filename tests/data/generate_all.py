"""Generate all FDTD data files from scratch.

Produces sigma_bond.py, sigma_ring.py, sigma_nnn.py, kappa_table.py at L=100.
Convergence verified: L=100 vs L=120 within 3.2% (tests/convergence_scan.py).

Run: cd tests/data && OPENBLAS_NUM_THREADS=1 OMP_NUM_THREADS=1 /usr/bin/python3 generate_all.py
Expected time: ~75 min (M3 MacBook Air).

Parameters: L=100, DW=15, DT=0.25, sx=8.0, r_m=20, K1=1.0, K2=0.5.

NOTE: sigma_bond at L=80 showed ~12% systematic bias across ALL k
(not only at k=0.3 as for sigma_ring). Two separate effects:
  1. PML contamination at low k (affects ring 44-51% at k=0.3)
  2. Uniform ~12% bias for single-bond scatterer (cause: near-field or
     box-size effect for small scatterer)
Both converge away at L=100 (max Δ vs L=120: bond 1.4%, ring R=9 3.2%).
See tests/convergence_scan.py for full data.
"""
import sys
import os
import numpy as np
import time

sys.path.insert(0, os.path.join(os.path.dirname(__file__), '..', '..', 'src'))

from elastic_3d import scalar_laplacian_3d, make_damping_3d, run_fdtd_3d
from gauge_3d import make_vortex_force
from scattering_3d import (make_wave_packet_3d, make_sphere_points,
                           compute_sphere_f2, integrate_sigma_3d,
                           estimate_n_steps_3d)

# ── Parameters ────────────────────────────────────────────────────
K1, K2 = 1.0, 0.5
L = 100
DW = 15
DS = 1.5
DT = 0.25
sx = 8.0
r_m = 20
THETAS = np.linspace(0, np.pi, 13)
PHIS = np.linspace(0, 2 * np.pi, 24, endpoint=False)

k_vals = np.array([0.3, 0.5, 0.7, 0.9, 1.1, 1.3, 1.5])

CENTER = L // 2
X_START = CENTER - 20

HEADER = f'''"""Generated by data/generate_all.py
Parameters: L={L}, DW={DW}, DT={DT}, sx={sx}, r_m={r_m}, K1={K1}, K2={K2}
"""
import numpy as np
'''


# ── Shared infrastructure ────────────────────────────────────────

def setup_fdtd():
    """Create shared FDTD objects."""
    gamma_pml = make_damping_3d(L, DW, DS)
    iz, iy, ix = make_sphere_points(L, r_m, THETAS, PHIS)
    return gamma_pml, iz, iy, ix


def force_plain(ux, uy, uz):
    return scalar_laplacian_3d(ux, uy, uz, K1, K2)


def compute_references(gamma_pml, iz, iy, ix):
    """Run reference (no defect) FDTD at all k values. Shared across datasets."""
    refs = {}
    for k0 in k_vals:
        ux0, vx0 = make_wave_packet_3d(L, k0, X_START, sx, K1, K2)
        ns = estimate_n_steps_3d(k0, L, X_START, sx, r_m, DT, 50, K1, K2)
        ref = run_fdtd_3d(force_plain, ux0.copy(), vx0.copy(), gamma_pml,
                          DT, ns, rec_iz=iz, rec_iy=iy, rec_ix=ix, rec_n=ns)
        refs[k0] = (ref, ux0, vx0, ns)
        print(f"    ref k={k0:.1f}: ns={ns}", flush=True)
    return refs


def run_sigma_tr(force_fn, refs, gamma_pml, iz, iy, ix):
    """Run defect FDTD and return sigma_tr array."""
    sigma_tr = np.zeros(len(k_vals))
    for i, k0 in enumerate(k_vals):
        ref, ux0, vx0, ns = refs[k0]
        d = run_fdtd_3d(force_fn, ux0.copy(), vx0.copy(), gamma_pml,
                        DT, ns, rec_iz=iz, rec_iy=iy, rec_ix=ix, rec_n=ns)
        f2 = compute_sphere_f2(d['ux'], d['uy'], d['uz'],
                               ref['ux'], ref['uy'], ref['uz'], r_m)
        _, st = integrate_sigma_3d(f2, THETAS, PHIS)
        sigma_tr[i] = st
        print(f"      k={k0:.1f}: σ_tr = {st:.3f}", flush=True)
    return sigma_tr


def run_sigma_tr_and_pol(force_fn, refs, gamma_pml, iz, iy, ix):
    """Single FDTD run → sigma_tr, sigma_xx, sigma_xy from same data."""
    sigma_tr = np.zeros(len(k_vals))
    sigma_xx = np.zeros(len(k_vals))
    sigma_xy = np.zeros(len(k_vals))
    for i, k0 in enumerate(k_vals):
        ref, ux0, vx0, ns = refs[k0]
        d = run_fdtd_3d(force_fn, ux0.copy(), vx0.copy(), gamma_pml,
                        DT, ns, rec_iz=iz, rec_iy=iy, rec_ix=ix, rec_n=ns)
        # Total f2
        f2 = compute_sphere_f2(d['ux'], d['uy'], d['uz'],
                               ref['ux'], ref['uy'], ref['uz'], r_m)
        _, st = integrate_sigma_3d(f2, THETAS, PHIS)
        sigma_tr[i] = st
        # Per-polarization f2 from same FDTD run (same inc2 as compute_sphere_f2)
        sc_ux = d['ux'] - ref['ux']
        sc_uy = d['uy'] - ref['uy']
        sc_uz = d['uz'] - ref['uz']
        inc2 = np.mean(ref['ux']**2 + ref['uy']**2 + ref['uz']**2, axis=0)
        inc2_floor = max(1e-30, 1e-12 * np.max(inc2))
        inc2 = np.maximum(inc2, inc2_floor)
        f2_xx = r_m**2 * np.mean(sc_ux**2, axis=0) / inc2
        f2_xy = r_m**2 * np.mean(sc_uy**2, axis=0) / inc2
        f2_xz = r_m**2 * np.mean(sc_uz**2, axis=0) / inc2
        _, st_xx = integrate_sigma_3d(f2_xx, THETAS, PHIS)
        _, st_xy = integrate_sigma_3d(f2_xy, THETAS, PHIS)
        _, st_xz = integrate_sigma_3d(f2_xz, THETAS, PHIS)
        sigma_xx[i] = st_xx
        sigma_xy[i] = st_xy
        # Verify: σ_xx + σ_xy + σ_xz = σ_tr (same FDTD run, same normalization)
        assert abs(st_xx + st_xy + st_xz - st) / st < 0.01, \
            f"k={k0}: polarization sum {st_xx+st_xy+st_xz:.4f} ≠ σ_tr {st:.4f}"
        print(f"      k={k0:.1f}: σ_tr={st:.3f}, σ_xx={st_xx:.4f}, σ_xy={st_xy:.4f}, σ_xz={st_xz:.6f}",
              flush=True)
    return sigma_tr, sigma_xx, sigma_xy


def fmt_array(arr, decimals=3):
    """Format numpy array values as comma-separated string."""
    return ", ".join(f"{v:.{decimals}f}" for v in arr)


# ── File writers ─────────────────────────────────────────────────

def write_sigma_bond(sigma_bond, sigma_xx, sigma_xy):
    """Write sigma_bond.py."""
    lines = [HEADER]
    lines.append(f"k_vals = np.array([{fmt_array(k_vals)}])")
    lines.append("")
    lines.append("# Total per-bond sigma_tr")
    lines.append(f"sigma_bond = np.array([{fmt_array(sigma_bond, 4)}])")
    lines.append("")
    lines.append("# Polarization-resolved (alpha=0.30)")
    lines.append(f"sigma_xx = np.array([{fmt_array(sigma_xx, 4)}])")
    lines.append(f"sigma_xy = np.array([{fmt_array(sigma_xy, 4)}])")
    lines.append("")
    lines.append("# ── Self-consistency ─────────────────────────────────────────────")
    lines.append("# sigma_xx + sigma_xy ≈ sigma_bond (sigma_xz = 0: single z-bond, uz decoupled)")
    lines.append('assert all(np.abs(sigma_xx + sigma_xy - sigma_bond) < 0.001), \\')
    lines.append('    "sigma_xx + sigma_xy ≠ sigma_bond beyond rounding"')
    lines.append("assert len(sigma_bond) == len(k_vals) == 7")
    lines.append('assert all(sigma_bond > 0), "sigma_bond must be positive"')
    lines.append('assert np.mean(sigma_xx) > np.mean(sigma_xy), \\')
    lines.append('    "sigma_xx should dominate sigma_xy on average"')
    lines.append("")

    path = os.path.join(os.path.dirname(__file__), "sigma_bond.py")
    with open(path, "w") as f:
        f.write("\n".join(lines))
    print(f"  Written: {path}", flush=True)


def write_sigma_ring(sigma_ring, sigma_alpha_nn):
    """Write sigma_ring.py. Uses only existing keys (safe for checkpoints)."""
    R_keys = sorted(sigma_ring.keys())
    a_keys = sorted(sigma_alpha_nn.keys())

    lines = [HEADER]
    lines.append(f"k_vals = np.array([{fmt_array(k_vals)}])")
    lines.append("")
    lines.append("# sigma_tr(k) at alpha=0.30, NN gauging, various R")
    lines.append("sigma_ring = {")
    for R in R_keys:
        lines.append(f"    {R}: np.array([{fmt_array(sigma_ring[R])}]),")
    lines.append("}")
    lines.append("")
    lines.append("# sigma_tr(k) at various alpha, R=5, NN gauging")
    lines.append("sigma_alpha_nn = {")
    for a in a_keys:
        lines.append(f"    {a:.2f}: np.array([{fmt_array(sigma_alpha_nn[a])}]),")
    lines.append("}")
    lines.append("")
    lines.append("# ── Self-consistency ─────────────────────────────────────────────")
    # Only add cross-checks if data is complete
    if 5 in sigma_ring and 0.30 in sigma_alpha_nn:
        lines.append("np.testing.assert_array_equal(sigma_ring[5], sigma_alpha_nn[0.30],")
        lines.append('                              err_msg="sigma_ring[5] ≠ sigma_alpha_nn[0.30]")')
    if len(R_keys) >= 4:
        lines.append("# sigma grows with R at fixed k")
        lines.append("for _ik in range(7):")
        lines.append("    for _R1, _R2 in [(3, 5), (5, 7), (7, 9)]:")
        lines.append("        assert sigma_ring[_R2][_ik] > sigma_ring[_R1][_ik], \\")
        lines.append('            f"sigma not monotonic in R at k index {_ik}: R={_R1}→{_R2}"')
    if len(a_keys) >= 2:
        alpha_pairs = [(a_keys[i], a_keys[i+1]) for i in range(len(a_keys)-1)]
        lines.append("# sigma grows with alpha at fixed k")
        lines.append("for _ik in range(7):")
        lines.append(f"    for _a1, _a2 in {alpha_pairs}:")
        lines.append("        assert sigma_alpha_nn[_a2][_ik] > sigma_alpha_nn[_a1][_ik], \\")
        lines.append('            f"sigma not monotonic in alpha at k index {_ik}: α={_a1}→{_a2}"')
    lines.append("")

    path = os.path.join(os.path.dirname(__file__), "sigma_ring.py")
    with open(path, "w") as f:
        f.write("\n".join(lines))
    print(f"  Written: {path}", flush=True)


