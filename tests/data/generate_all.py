# ── Generators ────────────────────────────────────────────────────

def generate_sigma_bond(refs, gamma_pml, iz, iy, ix):
    """Single FDTD run → sigma_bond + polarization decomposition."""
    print("\n>>> sigma_bond (single z-bond, α=0.30)", flush=True)
    f_def = make_vortex_force(0.30, 0, L, K1, K2)
    # Verify R_loop=0 gives exactly 1 gauged bond (need gradient field, not const)
    u_test = np.zeros((L, L, L))
    for iz_t in range(L):
        u_test[iz_t, :, :] = iz_t * 0.01  # gradient in z → Δu ≠ 0 on z-bonds
    z_test = np.zeros((L, L, L))
    fx, _, _ = f_def(u_test, z_test, z_test)
    fx_plain, _, _ = force_plain(u_test, z_test, z_test)
    diff = np.max(np.abs(fx - fx_plain))
    n_gauged = np.sum(np.abs(fx - fx_plain) > 1e-6)
    assert diff > 1e-4, f"R_loop=0 gives no gauged bonds (diff={diff:.2e})"
    assert n_gauged == 2, f"R_loop=0 gave {n_gauged} gauged sites (expected 2: one bond, two sites)"
    print(f"  R_loop=0 verified: {n_gauged} sites = {n_gauged//2} bond(s), diff={diff:.4f}", flush=True)
    sigma_bond, sigma_xx, sigma_xy = run_sigma_tr_and_pol(
        f_def, refs, gamma_pml, iz, iy, ix)
    write_sigma_bond(sigma_bond, sigma_xx, sigma_xy)
    return sigma_bond


def generate_sigma_ring(refs, gamma_pml, iz, iy, ix):
    """R-scan + α-scan, NN gauging. Writes checkpoint after each dataset."""
    R_values = [3, 5, 7, 9]
    alpha_values = [0.10, 0.15, 0.20, 0.25, 0.30, 0.40, 0.50]

    # R scan at α=0.30
    sigma_ring = {}
    sigma_alpha_nn = {}
    for R in R_values:
        print(f"\n>>> sigma_ring R={R} (α=0.30)", flush=True)
        f_def = make_vortex_force(0.30, R, L, K1, K2)
        sigma_ring[R] = run_sigma_tr(f_def, refs, gamma_pml, iz, iy, ix)
        # Checkpoint after each R
        write_sigma_ring(sigma_ring, sigma_alpha_nn)

    # α scan at R=5
    for a in alpha_values:
        if a == 0.30:
            sigma_alpha_nn[a] = sigma_ring[5].copy()
            print(f"\n>>> sigma_alpha α={a} R=5: reused from sigma_ring[5]", flush=True)
        else:
            print(f"\n>>> sigma_alpha α={a} R=5", flush=True)
            f_def = make_vortex_force(a, 5, L, K1, K2)
            sigma_alpha_nn[a] = run_sigma_tr(f_def, refs, gamma_pml, iz, iy, ix)
        # Checkpoint: write after each dataset completes
        write_sigma_ring(sigma_ring, sigma_alpha_nn)

    return sigma_ring, sigma_alpha_nn


def generate_sigma_nnn(refs, gamma_pml, iz, iy, ix):
    """NNN gauging at multiple α. Writes checkpoint after each α."""
    alpha_values_nnn = [0.10, 0.15, 0.20, 0.25, 0.30]
    sigma_nnn = {}
    for a in alpha_values_nnn:
        print(f"\n>>> sigma_nnn α={a:.2f} (R=5, NNN gauging)", flush=True)
        f_def = make_vortex_force(a, 5, L, K1, K2, gauge_nnn=True)
        sigma_nnn[a] = run_sigma_tr(f_def, refs, gamma_pml, iz, iy, ix)
        # Checkpoint: write after each alpha completes
        write_sigma_nnn(sigma_nnn)
    return sigma_nnn


def generate_kappa_table(sigma_alpha_nn, sigma_nnn):
    """Compute kappa from sigma data (post-processing, no FDTD)."""
    print("\n>>> kappa_table (post-processing)", flush=True)
    N_POL = 2
    R = 5
    K_CUTOFFS = [0.9, 1.5]

    def compute_kappa(sigma_arr, R_loop, cutoffs):
        prefactor = N_POL * R_loop / (4 * np.pi**2)
        integrand = np.sin(k_vals)**2 * sigma_arr
        kappas = []
        for kc in cutoffs:
            mask = k_vals <= kc + 0.01
            # trapz on uniform Δk=0.2 grid: error O(Δk²) ≈ 4%
        kappas.append(prefactor * np.trapz(integrand[mask], k_vals[mask]))
        return tuple(kappas)

    kappa_nn = {}
    for a in sorted(sigma_alpha_nn.keys()):
        kappa_nn[a] = compute_kappa(sigma_alpha_nn[a], R, K_CUTOFFS)
        print(f"  κ_NN(α={a:.2f}): k≤0.9={kappa_nn[a][0]:.3f}, k≤1.5={kappa_nn[a][1]:.3f}",
              flush=True)

    kappa_nnn = {}
    for a in sorted(sigma_nnn.keys()):
        kappa_nnn[a] = compute_kappa(sigma_nnn[a], R, K_CUTOFFS)
        print(f"  κ_NNN(α={a:.2f}): k≤0.9={kappa_nnn[a][0]:.3f}, k≤1.5={kappa_nnn[a][1]:.3f}",
              flush=True)

    write_kappa_table(kappa_nn, kappa_nnn)


# ── Main ─────────────────────────────────────────────────────────

if __name__ == "__main__":
    t_total = time.time()
    print(f"generate_all.py: L={L}, {len(k_vals)} k-pts")
    print(f"Parameters: DW={DW}, DT={DT}, sx={sx}, r_m={r_m}, K1={K1}, K2={K2}")

    gamma_pml, iz, iy, ix = setup_fdtd()

    print("\n>>> Computing references (shared across all datasets)...", flush=True)
    refs = compute_references(gamma_pml, iz, iy, ix)

    # 1. sigma_bond (single z-bond + polarization, single FDTD run)
    sigma_bond = generate_sigma_bond(refs, gamma_pml, iz, iy, ix)

    # 2. sigma_ring (R-scan + α-scan, with checkpoints)
    sigma_ring, sigma_alpha_nn = generate_sigma_ring(refs, gamma_pml, iz, iy, ix)

    # 3. sigma_nnn (NNN gauging, multiple α, with checkpoints)
    sigma_nnn = generate_sigma_nnn(refs, gamma_pml, iz, iy, ix)

    # 4. kappa_table (post-processing from sigma, no FDTD)
    generate_kappa_table(sigma_alpha_nn, sigma_nnn)

    dt = (time.time() - t_total) / 60
    print(f"\n\nTotal time: {dt:.1f} min")
    print("Done. All data files regenerated.")
